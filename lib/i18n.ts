// ── Translation map ────────────────────────────────────────────────────────────
export const TRANSLATIONS: Record<Locale, Translations> = { en, fr, de, nl, es, pt, he, ar };

// ── Helper to detect locale from browser ──────────────────────────────────────
export function detectLocale(): Locale {
  if (typeof window === "undefined") return "en";
  try {
    const stored = localStorage.getItem("cupclash_locale") as Locale | null;
    if (stored && stored in LOCALES) return stored;
  } catch {}
  const lang = navigator.language.split("-")[0] as Locale;
  return lang in LOCALES ? lang : "en";
}

// Server-side counterpart to detectLocale — matches an IETF language tag
// (e.g. Telegram's message.from.language_code, "en-US") against the app's
// supported locales by primary subtag, falling back to English.
export function localeFromLanguageCode(languageCode: string | null | undefined): Locale {
  const primary = (languageCode ?? "en").split("-")[0].toLowerCase();
  return primary in LOCALES ? (primary as Locale) : "en";
}

// ── Template string interpolation ────────────────────────────────────────────
export function interpolate(str: string, vars: Record<string, string | number>): string {
  return str.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? `{${key}}`));
}
