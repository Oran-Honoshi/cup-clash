-- ============================================================
-- Migration 069 — Country-follow model.
--
-- Adds a general `countries` reference table (ISO 3166-1 alpha-2, all
-- codes with an existing /public/flags/{code}.svg asset, minus a handful
-- of uninhabited/no-football-federation territories) — NOT the 32-nation
-- World Cup subset already hardcoded in lib/countries.ts, which stays as
-- the WC-specific list it always was.
--
-- Widens user_follows.followed_type to allow 'country' alongside the
-- existing 'team'/'competition' values, and adds competitions.country
-- (nullable text) so a country-follow can be joined against domestic
-- leagues. Backfilling that column for currently-tracked leagues is a
-- separate step later in this same migration (see bottom).
-- ============================================================

create table if not exists public.countries (
  id         uuid default gen_random_uuid() primary key,
  code       text not null unique, -- lowercase FlagBadge/flag.tsx code: ISO 3166-1 alpha-2, or a "gb-xxx" UK-home-nation subdivision code (see below) — matches /public/flags/{code}.svg either way
  name       text not null,
  flag       text not null,        -- flag asset code; own column so callers don't need to assume flag===code
  created_at timestamptz default now()
);

alter table public.countries enable row level security;

create policy "Countries are publicly readable"
  on public.countries for select using (true);

