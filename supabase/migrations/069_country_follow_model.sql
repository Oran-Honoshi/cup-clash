-- ── competitions.country ────────────────────────────────────────────────
-- Free-text, matched against countries.name (same "match by name, not FK"
-- convention TRACKED_LEAGUES already uses against competitions.name) —
-- simplest way to join "is this competition's country followed" without
-- a migration to backfill a real countries_id FK across existing rows.
alter table public.competitions add column if not exists country text;

-- ── user_follows.followed_type widen ────────────────────────────────────
alter table public.user_follows drop constraint if exists user_follows_followed_type_check;
alter table public.user_follows add constraint user_follows_followed_type_check
  check (followed_type in ('team', 'competition', 'country'));

